export const ThemeList = Object.keys(Theme).map((key) => {
  return {
    label: key,
    value: key,
    color: Theme[key as keyof typeof Theme][':root']['--primary']
  };
});
export function changeTheme(theme: keyof typeof Theme) {
  if (!Theme[theme]) return;

  const themeValue = Theme[theme];
  const themeStyleId = 'theme-vars';

  // 获取或创建样式标签
  let themeStyle = document.getElementById(
    themeStyleId
  ) as HTMLStyleElement | null;

  if (!themeStyle) {
    themeStyle = document.createElement('style');
    themeStyle.id = themeStyleId;
    document.head.appendChild(themeStyle);
  }

  // 创建完整的 CSS 规则，包括 :root 和 .dark 选择器
  let cssRules = '';

  // 添加 :root 选择器的 CSS 变量
  if (themeValue[':root']) {
    cssRules += ':root {\n';
    Object.entries(themeValue[':root']).forEach(([key, value]) => {
      cssRules += `  ${key}: ${value as string};\n`;
    });
    cssRules += '}\n\n';
  }

  // 添加 .dark 选择器的 CSS 变量
  if (themeValue['.dark']) {
    cssRules += '.dark {\n';
    Object.entries(themeValue['.dark']).forEach(([key, value]) => {
      cssRules += `  ${key}: ${value as string};\n`;
    });
    cssRules += '}';
  }

  // 将 CSS 规则设置到样式标签中
  themeStyle.textContent = cssRules;
}
export function cssVarsToJson(cssString: string) {
  // 为 result 对象添加类型注解，使其可以接受字符串索引
  const result: { [key: string]: { [key: string]: string } } = {};

  // 分割不同的选择器块
  const blocks = cssString.split(/(?=\.[a-zA-Z]|:\.[a-zA-Z])/);

  blocks.forEach((block) => {
    if (!block.trim()) return;

    // 提取选择器名称
    const selectorMatch = block.match(/^([^{]+)\s*{/);
    if (!selectorMatch) return;

    const selector = selectorMatch[1].trim();
    const content = block.substring(
      selectorMatch[0].length,
      block.lastIndexOf('}')
    );

    // 为 vars 对象添加类型注解，使其可以接受字符串索引
    const vars: { [key: string]: string } = {};
    const varRegex = /--([a-zA-Z0-9-]+)\s*:\s*([^;]+);/g;
    let match;

    while ((match = varRegex.exec(content)) !== null) {
      vars[`--${match[1]}`] = match[2].trim();
    }

    result[selector] = vars;
  });

  return JSON.stringify(result, null, 2);
}
